    /**
     * Google Drive„ÅÆURL„Åã„Çâ„Éï„Ç°„Ç§„É´ID„ÇíÊäΩÂá∫
     * @param {string} url - Google Drive URL
     * @returns {string|null} „Éï„Ç°„Ç§„É´ID
     */
    extractFileIdFromUrl(url) {
        // „Éë„Çø„Éº„É≥1: https://drive.google.com/file/d/{fileId}/view
        let match = url.match(/\/file\/d\/([^\/]+)/);
        if (match) return match[1];

        // „Éë„Çø„Éº„É≥2: https://drive.google.com/open?id={fileId}
        match = url.match(/[?&]id=([^&]+)/);
        if (match) return match[1];

        // „Éë„Çø„Éº„É≥3: https://drive.google.com/uc?id={fileId}
        match = url.match(/\/uc\?id=([^&]+)/);
        if (match) return match[1];

        // „Éë„Çø„Éº„É≥4: „Éï„Ç°„Ç§„É´ID„Åù„ÅÆ„ÇÇ„ÅÆ
        if (/^[a-zA-Z0-9_-]{25,}$/.test(url)) {
            return url;
        }

        return null;
    }

