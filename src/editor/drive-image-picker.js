                    'Error / „Ç®„É©„Éº',
                    '„Åì„ÅÆ„Éï„Ç°„Ç§„É´„ÅØÁîªÂÉè„Åß„ÅØ„ÅÇ„Çä„Åæ„Åõ„Çì / This file is not an image',
                    'OK',
